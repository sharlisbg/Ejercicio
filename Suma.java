public class Suma{

    /*
     * Constructor de Sumas
     */
    public Suma() {
    }

    
    /** 
     * @param num1
     * @param num2
     * @return int
     */
    public int Sumar(int num1, int num2){
        int suma = num1 + num2;
        return suma;
    }
}