 public class Resta{

    /*
     * Constructor de Sumas
     */
    public Resta() {
    }

    
    /** 
     * @param num1
     * @param num2
     * @return int
     */
    public int Restar (int num1, int num2){
        int resta = num1 - num2;
        return resta;
    }

 }