 public class Multiplicacion{

    /*
     * Constructor de Sumas
     */
    public Multiplicacion() {
    }

    
    /** 
     * @param num1
     * @param num2
     * @return int
     */
    public int Multiplicar(int num1, int num2){
        int mult = num1*num2;
        return mult;
    }
 }

